      SUBROUTINE DROP(OBJECT,WHERE) 
  
*  PLACE AN OBJECT AT A GIVEN LOC, PREFIXING IT ONTO THE ATLOC LIST. DECREASING 
*  HOLDNG IF THE OBJECT WAS BEING TOTED.
  
      IMPLICIT INTEGER(A-Z) 
      COMMON /PLACOM/ ATLOC,LINK,PLACE,FIXED,COND,PROP,LOC,LAMP,HOLDNG
      DIMENSION PROP(100), LINK(200), PLACE(100), FIXED(100)
      DIMENSION ATLOC(220), COND(220) 
  
      IF(OBJECT.GT.100)GOTO 1 
      IF(PLACE(OBJECT).EQ.-1)HOLDNG=HOLDNG-1
      PLACE(OBJECT)=WHERE 
      GOTO 2
    1 FIXED(OBJECT-100)=WHERE 
    2 IF(WHERE.LE.0)RETURN
      LINK(OBJECT)=ATLOC(WHERE) 
      ATLOC(WHERE)=OBJECT 
      RETURN
      END 

      INTEGER FUNCTION VOCAB(ID,INIT) 
  
*  LOOK UP ID IN THE VOCABULARY (ATAB) AND RETURN ITS 'DEFINITION' (KTAB
*  -1 IF NOT FOUND.  IF INIT IS POSITIVE, THIS IS AN INITIALIZATION CALL
*  UP A KEYWORD VARIABLE, AND NOT FINDING IT CONSTITUTES A BUG.  IT ALSO
*  THAT ONLY KTAB VALUES WHICH TAKEN OVER 1000 EQUAL INIT MAY BE CONSIDE
*  (THUS 'STEPS', WHICH IS A MOTION VERB AS WELL AS AN OBJECT, MAY BE LO
*  AS AN OBJECT.)  AND IT ALSO MEANS THE KTAB VALUE IS TAKEN MOD 1000.
  
      IMPLICIT INTEGER(A-Z) 
      COMMON /VOCCOM/ TABSIZ,KTAB,ATAB
      Character*10 ID, HASH, ATAB
      DIMENSION KTAB(400), ATAB(400)
  
!!!!! HASH=SCRMBL(ID) 
      HASH = ID
      DO 1 I=1,TABSIZ 
      IF(KTAB(I).EQ.-1)GOTO 2 
      IF(INIT.GE.0.AND.KTAB(I)/1000.NE.INIT)GOTO 1
      IF(ATAB(I).EQ.HASH)GOTO 3 
    1 CONTINUE
      CALL BUG(21)
  
    2 VOCAB=-1
      IF(INIT.LT.0)RETURN 
      PRINT*,HASH 
      CALL BUG(5) 
  
    3 VOCAB=KTAB(I) 
      IF(INIT.GE.0)VOCAB=MOD(VOCAB,1000)
      RETURN
      END 

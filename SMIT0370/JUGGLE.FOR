      SUBROUTINE JUGGLE(OBJECT) 
  
*  JUGGLE AN OBJECT BY PICKING IT UP AND PUTTING IT DOWN AGAIN, THE PURP
*  BEING TO GET THE OBJECT TO THE FRONT OF THE CHAIN OF THINGS AT ITS LO
  
      IMPLICIT INTEGER(A-Z) 
      COMMON /PLACOM/ ATLOC,LINK,PLACE,FIXED,COND,PROP,LOC,LAMP,HOLDNG
      DIMENSION PROP(100), LINK(200), PLACE(100), FIXED(100)
      DIMENSION ATLOC(220), COND(220) 
  
      I=PLACE(OBJECT) 
      J=FIXED(OBJECT) 
      CALL MOVE(OBJECT,I) 
      CALL MOVE(OBJECT+100,J) 
      RETURN
      END 

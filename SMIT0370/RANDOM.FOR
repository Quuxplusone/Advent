      INTEGER FUNCTION RANDOM(RANGE) 
  
*  RETURN RANDOM UNIFORMLY DISTRIBUTED VALUE IN CLOSED INTERVAL(0,RANGE). 
* 
*  COURTESY MICHAEL GRUBIN@PRIME COMPUTER INC., FRAMINGHAM MASS.

  
      IMPLICIT INTEGER (A-Z)
      COMMON /SEED/ SEED

      RANDOM = (RANGE-1.)*RAN(SEED)
      RETURN
      END 

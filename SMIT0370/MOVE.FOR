      SUBROUTINE MOVE(OBJECT,WHERE) 
  
*  PLACE ANY OBJECT ANYWHERE BY PICKING IT UP AND DROPPING IT.  MAY ALREADY 
*  TOTING, IN WHICH CASE THE CARRY IS A NO-OP.  MUSTN'T PICK UP OBJECTS THAT
*  ARE NOT AT ANY LOC, SINCE CARRY WANTS TO REMOVE OBJECTS FROM ATLOC CH
  
      IMPLICIT INTEGER(A-Z) 
      COMMON /PLACOM/ ATLOC,LINK,PLACE,FIXED,COND,PROP,LOC,LAMP,HOLDNG
      DIMENSION PROP(100), LINK(200), PLACE(100), FIXED(100)
      DIMENSION ATLOC(220), COND(220) 
  
      IF(OBJECT.GT.100)GOTO 1 
      FROM=PLACE(OBJECT)
      GOTO 2
    1 FROM=FIXED(OBJECT-100)
    2 IF(FROM.GT.0.AND.FROM.LE.300)CALL CARRY(OBJECT,FROM)
      CALL DROP(OBJECT,WHERE) 
      RETURN
      END 

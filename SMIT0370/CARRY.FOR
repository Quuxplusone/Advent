      SUBROUTINE CARRY(OBJECT,WHERE)
  
*  START TOTING AN OBJECT, REMOVING IT FROM THE LIST OF THINGS AT ITS FO
*  LOCATION.  INCR HOLDNG UNLESS IT WAS ALREADY BEING TOTED.  IF OBJECT>
*  (MOVING 'FIXED' SECOND LOC), DON'T CHANGE PLACE OR HOLDNG. 
  
      IMPLICIT INTEGER(A-Z) 
      COMMON /PLACOM/ ATLOC,LINK,PLACE,FIXED,COND,PROP,LOC,LAMP,HOLDNG
      DIMENSION PROP(100), LINK(200), PLACE(100), FIXED(100)
      DIMENSION ATLOC(220), COND(220) 
  
      IF(OBJECT.GT.100)GOTO 5 
      IF(PLACE(OBJECT).EQ.-1)RETURN 
      PLACE(OBJECT)=-1
      HOLDNG=HOLDNG+1 
    5 IF(ATLOC(WHERE).NE.OBJECT)GOTO 6
      ATLOC(WHERE)=LINK(OBJECT) 
      RETURN
    6 TEMP=ATLOC(WHERE) 
    7 IF(LINK(TEMP).EQ.OBJECT)GOTO 8
      TEMP=LINK(TEMP) 
      GOTO 7
    8 LINK(TEMP)=LINK(OBJECT) 
      RETURN
      END 
